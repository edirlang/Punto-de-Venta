    private void onloadProducts(){
        for(Product product : this.productDao.getProductsSoldOut()){
            ProductView view = new ProductView(product);
            view.setVisible(true);
            this.panel_products.add(view);
            
        }
    }
    
    private void onloadCustomerHappyBirth(){
        for(Clientes customer : this.customerDao.getConsumersHappyBirthDay()){
            JLabel label = new JLabel("Cedula: "+customer.getDocumentNumber() +" - "+customer.getFullName());
            label.setVisible(true);
            this.panel_customer.add(label);
        }
    }
    
    private void onloadUserGraphits(){
        DefaultCategoryDataset  dataset = new DefaultCategoryDataset();
        for(Object[] item: this.productDao.getProductMoreSale()){
            dataset.addValue(Integer.parseInt(item[1].toString()), item[0].toString(), item[0].toString());
        }
        JFreeChart chart = ChartFactory.createBarChart(
            "Productos mas Vendidos",
            "Productos",
            "Cantidad",
            dataset,
            PlotOrientation.HORIZONTAL,
            true,
            true,
            true
          );
        ChartPanel chartPanel = new ChartPanel(chart, false);
        this.panel_graphits.add(chartPanel);
    }
    private void onloadFormConfig(){
        List<Config> configs = MenuUsuario.mainCtrl.getAllConfig();
        for(Config config: configs){
            if("name_company".equals(config.getName())){
                this.txt_name.setText(config.getValue());
            }else if("nit_company".equals(config.getName())){
                this.txt_nit.setText(config.getValue());
            }else if("company_phone".equals(config.getName())){
                this.txt_phone.setText(config.getValue());
            }else if("price_point".equals(config.getName())){
                this.txt_points.setText(config.getValue());
            }
            
        }
    }

